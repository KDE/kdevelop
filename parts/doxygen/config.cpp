void ConfigOption::writeStringValue(QTextStream &t,QCString &s)
{
  const char *p=s.data();
  char c;
  bool needsEscaping=FALSE;
  if (p)
  {
    while ((c=*p++)!=0 && !needsEscaping)
      needsEscaping = (c==' ' || c=='\n' || c=='\t' || c=='"');
    if (needsEscaping)
    {
      t << "\"";
      p=s.data();
      while (*p)
      {
	if (*p=='"') t << "\\"; // escape quotes
	t << *p++;
      }
      t << "\"";
    }
    else
    {
      t << s;
    }
  }
}

